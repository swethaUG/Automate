//Demo for Increment/Decrement Operators
package com.javaprograms;

public class IncDecOperators {

	public static void main(String[] args) {
		int n= 5;
		System.out.println(n);
		n = n+1;
		System.out.println(n);
		n++;
		System.out.println(n);
		n = n-1;
		System.out.println(n);
		n--;
		System.out.println(n);

	}

}
